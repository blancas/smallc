// Integer arithmetic.

int add(int a, int b) {
  return a + b;
}

int plus(int a, int b) {
  int plus = a + b;
  return plus;
}

int sub(int a, int b) {
  return a - b;
}

int minus(int a, int b) {
  int minus = a - b;
  return minus;
}

int mul(int a, int b) {
  return a * b;
}

int star(int a, int b) {
  int star = a * b;
  return star;
}

int div(int a, int b) {
  return a / b;
}

int slash(int a, int b) {
  int slash = a / b;
  return slash;
}

int mod(int a, int b) {
  return a % b;
}

int percent(int a, int b) {
  int percent = a % b;
  return percent;
}
