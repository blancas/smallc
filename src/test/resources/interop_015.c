// Long arithmetic.

long add(long a, long b) {
  return a + b;
}

long plus(long a, long b) {
  long plus = a + b;
  return plus;
}

long sub(long a, long b) {
  return a - b;
}

long minus(long a, long b) {
  long minus = a - b;
  return minus;
}

long mul(long a, long b) {
  return a * b;
}

long star(long a, long b) {
  long star = a * b;
  return star;
}

long div(long a, long b) {
  return a / b;
}

long slash(long a, long b) {
  long slash = a / b;
  return slash;
}

long mod(long a, long b) {
  return a % b;
}

long percent(long a, long b) {
  long percent = a % b;
  return percent;
}
