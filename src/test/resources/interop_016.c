// Float arithmetic.

float add(float a, float b) {
  return a + b;
}

float plus(float a, float b) {
  float plus = a + b;
  return plus;
}

float sub(float a, float b) {
  return a - b;
}

float minus(float a, float b) {
  float minus = a - b;
  return minus;
}

float mul(float a, float b) {
  return a * b;
}

float star(float a, float b) {
  float star = a * b;
  return star;
}

float div(float a, float b) {
  return a / b;
}

float slash(float a, float b) {
  float slash = a / b;
  return slash;
}

float mod(float a, float b) {
  return a % b;
}

float percent(float a, float b) {
  float percent = a % b;
  return percent;
}
